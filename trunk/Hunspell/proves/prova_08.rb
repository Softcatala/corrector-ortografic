diec = entrades('diec')
diec2 = entrades('diec2')
rdiec = ['// mots invariables']
rdiec2 = ['// mots invariables']
bons, dolents = 0, 0
resultat = []
numlin = 8
mots.each do |mot|
	numlin += 1
	next if mot.empty?
	nota = ""
	if mot =~ /^(.*?)\s*<-+\s*(.*)/
		mot, nota = $1, " (#{$2})"
	end
	if !hs.OK(mot)
		resultat << "No es troba el mot #{mot}#{nota}"
		dolents += 1
		next
	end
	if hs.OK("#{mot}os")
		d = diec.has_key?(mot)
		d2 = diec2.has_key?(mot)
		if d && !d2
			rdiec << "#{mot}: PLU=#{mot}"
			bons += 1
		elsif !d && d2
			rdiec2 << "#{mot}: PLU=#{mot}"
			bons += 1
		elsif d && d2
			resultat << "#{mot} dins diec i diec2"
			dolents += 1
		else
			resultat << "#{mot} no dins diec i diec2"
			dolents += 1
		end
		#~ resultat << "Fals positiu: #{mot}os#{nota}"
		#~ resultat << "#{$0}:#{numlin}:#{mot}os#{nota}"
		#~ dolents += 1
		#~ next
	end
	bons += 1
end
puts ">>> #{dolents} errors / #{bons + dolents} intents"
puts resultat
